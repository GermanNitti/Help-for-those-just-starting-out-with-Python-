a = 10
b = 5

# Igual que
print(a == b)  # Output: False

# No igual que
print(a != b)  # Output: True

# Mayor que
print(a > b)  # Output: True

# Menor que
print(a < b)  # Output: False

# Mayor o igual que
print(a >= b)  # Output: True

# Menor o igual que
print(a <= b)  # Output: False
