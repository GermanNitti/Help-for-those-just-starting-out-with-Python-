#OPERADORES DE SUMA, RESTA, MULTIPLICACION Y DIVISION:

a = 10
b = 5

# Suma
suma = a + b
print("Suma:", suma)  # Output: 15

# Resta
resta = a - b
print("Resta:", resta)  # Output: 5

# Multiplicación
multiplicacion = a * b
print("Multiplicación:", multiplicacion)  # Output: 50

# División
division = a / b
print("División:", division)  # Output: 2.0 (en Python 3.x, la división de enteros produce un resultado de punto flotante)

#-------------------------------


#OPERADORES DE DIVISION ENTERA Y MODULO:

a = 10
b = 3

# División entera
division_entera = a // b
print("División Entera:", division_entera)  # Output: 3

# Módulo (residuo de la división)
modulo = a % b
print("Módulo:", modulo)  # Output: 1

#-------------------------------


#OPERADOR DE EXPONENTE:

a = 2
b = 3

# Exponente
resultado = a ** b
print("Exponente:", resultado)  # Output: 8 (2 elevado a la 3)
